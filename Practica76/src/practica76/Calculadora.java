public class Calculadora {
    public float suma(float num1, float num2) {
		return num1 + num2;
	}     
    public float resta(float num1, float num2) {
		return num1 - num2;
	}     
    public float multi(float num1, float num2) {
		return num1 * num2;
	}     
    public float divi(float num1, float num2) {
		return num1 / num2;
	}
}
